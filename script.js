/* ===== 正規化 ===== */
function normalize(str) {
  return (str || "")
    .toLowerCase()
    .replace(/[ぁ-ん]/g, s =>
      String.fromCharCode(s.charCodeAt(0) + 0x60)
    )
    .replace(/ー/g, "")
    .replace(/\s+/g, "");
}

/* ===== 初期化 ===== */
window.addEventListener("DOMContentLoaded", () => {

  const searchBox =
    document.getElementById("searchBox");
  const suggest =
    document.getElementById("suggest");
  const result =
    document.getElementById("result");
  const searchBtn =
    document.getElementById("searchBtn");

  let selectedIndex = -1;

  /* ===== 検索処理 ===== */
  function searchSong() {
    const keyword =
      searchBox.value.trim();

   const key = normalize(keyword);

result.innerHTML = "";

/* 空欄対策 */
if (!key) {
  result.innerHTML =
    "<li>見つかりませんでした</li>";
  return;
}

    const filtered =
      data.filter(item =>
        normalize(item.song) === key ||
        normalize(item.alias)
          .includes(key)
      );

    if (filtered.length === 0) {
      result.innerHTML =
        "<li>見つかりませんでした</li>";
      return;
    }

const grouped = {};

/* 年ごと分類 */
filtered.forEach(item => {

  const year =
    item.year || "その他";

  const liveText =
    item.note
      ? `${item.live}（${item.note}）`
      : item.live;

  if (!grouped[year]) {
    grouped[year] = new Set();
  }

  grouped[year].add(liveText);
});

/* 表示 */
Object.keys(grouped)
  .sort()
  .forEach(year => {

    // 年見出し
    const yearLi =
      document.createElement("li");

    yearLi.className =
      "year-title";

    yearLi.textContent =
      `[${year}]`;

    result.appendChild(yearLi);

    // ライブ一覧
    grouped[year]
      .forEach(live => {

        const liveLi =
          document.createElement("li");

        liveLi.className =
          "live-item";

        liveLi.textContent =
          live;

        result.appendChild(liveLi);
      });
  });

  }

  /* ===== サジェスト ===== */
  searchBox.addEventListener(
    "input",
    () => {

      const keyNorm =
        normalize(
          searchBox.value.trim()
        );

      suggest.innerHTML = "";
      selectedIndex = -1;

      if (!keyNorm) return;

      const matches = [];

      data.forEach(item => {
        if (
          normalize(item.song)
            .includes(keyNorm) ||
          normalize(item.alias)
            .includes(keyNorm)
        ) {
          matches.push(item.song);
        }
      });

      const uniqueMatches =
        [...new Set(matches)]
          .slice(0, 10);

      uniqueMatches.forEach(song => {

        const li =
          document.createElement("li");

        li.textContent = song;

        li.onclick = () => {
          searchBox.value = song;
          suggest.innerHTML = "";
          searchSong();
        };

        suggest.appendChild(li);
      });
    }
  );

  /* ===== 選択状態 ===== */
  function updateSelection(items) {

    items.forEach(item => {
      item.classList.remove(
        "selected"
      );
    });

    if (items[selectedIndex]) {

      items[selectedIndex]
        .classList.add(
          "selected"
        );

      items[selectedIndex]
        .scrollIntoView({
          block: "nearest"
        });
    }
  }

  /* ===== キーボード ===== */
  searchBox.addEventListener(
    "keydown",
    (e) => {

      const items =
        suggest.querySelectorAll(
          "li"
        );

      // ↓
      if (
        e.key === "ArrowDown"
      ) {

        e.preventDefault();

        if (
          items.length === 0
        ) return;

        selectedIndex =
          selectedIndex <
          items.length - 1
            ? selectedIndex + 1
            : 0;

        updateSelection(items);
      }

      // ↑
      else if (
        e.key === "ArrowUp"
      ) {

        e.preventDefault();

        if (
          items.length === 0
        ) return;

        selectedIndex =
          selectedIndex > 0
            ? selectedIndex - 1
            : items.length - 1;

        updateSelection(items);
      }

      // Enter
      else if (
        e.key === "Enter"
      ) {

        e.preventDefault();

        if (
          selectedIndex >= 0 &&
          items[selectedIndex]
        ) {
          searchBox.value =
            items[
              selectedIndex
            ].textContent;
        }

        suggest.innerHTML = "";
        searchSong();
      }
    }
  );


  /* ===== ボタン ===== */
  searchBtn.addEventListener(
    "click",
    searchSong
  );

});
