function normalize(str) {
  return (str || "")
    .toLowerCase()
    .replace(/[ぁ-ん]/g, s =>
      String.fromCharCode(s.charCodeAt(0) + 0x60)
    )
    .replace(/ー/g, "")
    .replace(/\s+/g, "");
}

