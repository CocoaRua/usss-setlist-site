window.addEventListener("DOMContentLoaded", () => {

  const searchBox = document.getElementById("searchBox");
  const suggest = document.getElementById("suggest");
  const result = document.getElementById("result");
  const searchBtn = document.getElementById("searchBtn");

  /* ===== 検索処理 ===== */
  function searchSong() {
    const keyword = searchBox.value.trim();
    const key = normalize(keyword);

    result.innerHTML = "";

    const filtered = data.filter(item =>
      normalize(item.song) === key ||
      normalize(item.alias).includes(key)
    );

    if (filtered.length === 0) {
      result.innerHTML = "<li>見つかりませんでした</li>";
      return;
    }

    const seen = new Set();

    filtered.forEach(item => {
      const text = item.note
        ? `${item.live}（${item.note}）`
        : item.live;

      if (!seen.has(text)) {
        seen.add(text);
        const li = document.createElement("li");
        li.textContent = text;
        result.appendChild(li);
      }
    });
  }

  /* ===== サジェスト ===== */
  searchBox.addEventListener("input", () => {
    const keyNorm = normalize(searchBox.value.trim());
    suggest.innerHTML = "";

    if (!keyNorm) return;

    const matches = [];

    data.forEach(item => {
      if (
        normalize(item.song).includes(keyNorm) ||
        normalize(item.alias).includes(keyNorm)
      ) {
        matches.push(item.song);
      }
    });

    [...new Set(matches)]
      .slice(0, 10)
      .forEach(song => {
        const li = document.createElement("li");
        li.textContent = song;

        li.onclick = () => {
          searchBox.value = song;
          suggest.innerHTML = "";
          searchSong();
        };

        suggest.appendChild(li);
      });
  });

  /* ===== ボタン ===== */
  searchBtn.addEventListener("click", searchSong);

  /* ===== Enter ===== */
  searchBox.addEventListener("keydown", (e) => {
    if (e.key === "Enter") {
      suggest.innerHTML = "";
      searchSong();
    }
  });

});
